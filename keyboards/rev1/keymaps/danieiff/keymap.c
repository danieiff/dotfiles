bool process_record_user(uint16_t keycode, keyrecord_t *record) {

  #ifdef CONSOLE_ENABLE
    uprintf("KL: kc: 0x%04X, col: %2u, row: %2u, pressed: %u, time: %5u, int: %u, count: %u\n", keycode, record->event.key.col, record->event.key.row, record->event.pressed, record->event.time, record->tap.interrupted, record->tap.count);
  #endif

  bool hold_start = !record->tap.count && record->event.pressed;
  bool hold_end = !record->tap.count && !record->event.pressed;
  bool tap_start = record->tap.count && record->event.pressed;
  // bool tap_end = record->tap.count && !record->event.pressed;
  switch (keycode) {
    case MPLY_VOLU:
      if (hold_start) {
        tap_code(KC_MPLY);
        return false;
      }
      break;
    case MUTE_VOLD:
      if (hold_start) {
        tap_code(KC_MUTE);
        return false;
      }
      break;
    case PSCR_DEL:
      if (hold_start) {
        tap_code(KC_PSCR);
        return false;
      }
      break;
    case SFT_ENT:
      if (hold_start) {
        tap_code16(LSFT(KC_ENT));
        return false;
      }
      break;
    case LT_LOWER:
      if (tap_start) {
        layer_invert(LOWER);
        return false;
      } else if (hold_start){
        layer_on(LOWER);
        return false;
      } else if (hold_end) {
        layer_off(LOWER);
        return false;
      }
      break;
    case ALT_T(EISU):
      if (tap_start) {
        tap_code16(LALT(KC_GRV));
        return false;
      }
      break;
  }

  return true;
}

